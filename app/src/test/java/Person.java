public class Person {

    private String name;
    private String email;
    private String phone;
    private Object avatar;
    private Object address;

    public Person(String name, String email, String phone, Object avatar, Object address) {
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.avatar = avatar;
        this.address = address;
    }
}
